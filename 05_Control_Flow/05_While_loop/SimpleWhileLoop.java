/*
 * Simple While Loop
 *
 * Demonstrates the use of a basic
 * while loop to print numbers
 * from 1 to 5.
 */

public class SimpleWhileLoop {

    public static void main(String[] args) {

        int count = 1;

        while (count <= 5) {
            System.out.println("Count = " + count);
            count++;
        }
    }
}