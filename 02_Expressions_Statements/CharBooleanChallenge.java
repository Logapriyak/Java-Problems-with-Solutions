/*
 * Char and Boolean Challenge
 *
 * Demonstrates Unicode characters
 * and boolean values.
 */

public class CharBooleanChallenge {

    public static void main(String[] args) {

        char unicodeCharacter = '\u00A9';
        boolean isCustomerOver21 = true;

        System.out.println(unicodeCharacter);
        System.out.println(isCustomerOver21);
    }
}
